package com.spring.boardweb;

public class UserVO {
	private String userId;
	private String userPw;
	private String userNm;
	private String userMail;
	private String userTel;
	private String userAdd;
	private String userAddDetail;
	private String userBnk;
	
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPw() {
		return userPw;
	}
	public void setUserPw(String userPw) {
		this.userPw = userPw;
	}
	public String getUserNm() {
		return userNm;
	}
	public void setUserNm(String userNm) {
		this.userNm = userNm;
	}
	public String getUserMail() {
		return userMail;
	}
	public void setUserMail(String userMail) {
		this.userMail = userMail;
	}
	public String getUserTel() {
		return userTel;
	}
	public void setUserTel(String userTel) {
		this.userTel = userTel;
	}
	public String getUserAdd() {
		return userAdd;
	}
	public void setUserAdd(String userAdd) {
		this.userAdd = userAdd;
	}
	public String getUserAddDetail() {
		return userAddDetail;
	}
	public void setUserAddDetail(String userAddDetail) {
		this.userAddDetail = userAddDetail;
	}
	public String getUserBnk() {
		return userBnk;
	}
	public void setUserBnk(String userBnk) {
		this.userBnk = userBnk;
	}

}
